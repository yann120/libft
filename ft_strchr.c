#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int				i;
	unsigned char	*str;

	i = 0;
	str = (unsigned char *)s;
	while (str)
	{
		if (str[i] == (unsigned char)c)
			return (((char *)str));
		str++;
	}
	return (NULL);
}
