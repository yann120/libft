#include "libft.h"

int		ft_strncmp(const char *s1, const char *s2, size_t n)
{
	size_t i;

	i = 0;
	while (s1[i] && i < n)
	{
		if (s1[i] != s2[i])
			return ((unsigned char)s1[i] - (unsigned char)s2[i]);
		i++;
	}
	if (s1[0] == 0)
		return (-s2[0]);
	else if (s1[i] == 0 && i < n)
		return (-s2[i]);
	return (0);
}
